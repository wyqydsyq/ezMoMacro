toggle_key = 'ctrl+F12' # key to use for toggling the macro on/off
action_key = 'e' # key to use for macro action, generally will be E for self-casting, Q for target-casting and LMB for swinging
action_duration = 2 # how long to wait between triggering each action (should be roughly the time it takes to perform the action ingame)
cycle_length = 10 # amount of times to repeat action each cycle
rest_key = '0' # key to use for resting, this should be whatever hotkey you bind rest to ingame
rest_interval = 5 # amount of seconds to rest for between each action cycle, set to 0 to disable resting